package org.djjs.model;

public class AddressVO {

    String ashramID, description, stateID, name, phone, zip, headedBy;

    /**
     * @return the headedBy
     */
    public String getHeadedBy() {
        return headedBy;
    }

    /**
     * @param headedBy the headedBy to set
     */
    public void setHeadedBy(String headedBy) {
        this.headedBy = headedBy;
    }

    /**
     * @return the ashramID
     */
    public String getAshramID() {
        return ashramID;
    }

    /**
     * @param ashramID the ashramID to set
     */
    public void setAshramID(String ashramID) {
        this.ashramID = ashramID;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the stateID
     */
    public String getStateID() {
        return stateID;
    }

    /**
     * @param stateID the stateID to set
     */
    public void setStateID(String stateID) {
        this.stateID = stateID;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return the zip
     */
    public String getZip() {
        return zip;
    }

    /**
     * @param zip the zip to set
     */
    public void setZip(String zip) {
        this.zip = zip;
    }
    
}

