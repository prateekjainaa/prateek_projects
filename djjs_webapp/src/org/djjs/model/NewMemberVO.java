package org.djjs.model;

public class NewMemberVO extends MemberVO {

	private String fullofficeAddressText, designation, reference;

	public String getFullofficeAddressText() {
		return fullofficeAddressText;
	}

	public void setFullofficeAddressText(String fullofficeAddressText) {
		this.fullofficeAddressText = fullofficeAddressText;
	}

	public String getDesignation() {
		return designation;
	}

	public void setDesignation(String designation) {
		this.designation = designation;
	}

	public String getReference() {
		return reference;
	}

	public void setReference(String reference) {
		this.reference = reference;
	}
	
}
